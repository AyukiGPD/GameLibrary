//-----------------------------------------------------------------------------
//  @brief  fgetc
//  @param  [in]    fp  ファイルポインタ
//-----------------------------------------------------------------------------
u8 SafeFgetc(FILE* fp)
{
	u8 result = 0;
	s32 getChar = fgetc(fp);
	if (getChar != EOF)
	{
		result = u8(getChar & 255);
	}

	return result;
}
